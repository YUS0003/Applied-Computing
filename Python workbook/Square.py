# Display a title or heading for the program
print(" The room's area")

# Prompt the user to enter the length of the room and convert it to a float
length = float(input("Please enter the length of the room "))

# Prompt the user to enter the width of the room and convert it to a float
width = float(input("Please enter the width of the room "))

# Calculate the area of the room by multiplying length and width
area = length * width

# Display the calculated area of the room in square metres
print("The area of the room is", area ,"m2")
