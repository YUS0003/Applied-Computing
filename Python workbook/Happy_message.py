rating = int(input("On a scale of 1 to 10, how happy are you today? "))

if rating <= 3:
    print("I'm sorry you're feeling low. Remember, every storm passes. You've got this!")
    
elif rating <= 7:
    print("Sounds like an average day. Keep going - something great could be just around the corner!")
    
else:
    print("That's awesome! Keep smiling and spread that joy everywhere you go!")
